#!/usr/bin/env npx tsx
/**
 * Directus — Earnest Demo (Solo) org + user + seed data
 *
 * Provisions the solo-studio demo: one org, one Member-role user, a
 * believable slice of one-person-studio seed data. Anonymous visitors who
 * pick "Solo studio" on /try-demo land here.
 *
 * Idempotent: every create is guarded by a find-by-key check; re-running
 * the script never duplicates data.
 *
 * AI is capped via the existing org token budget (10K tokens/month + 3
 * scan credits/month). The Member role in this org has `ai_usage` bumped
 * to access+read+create so demo visitors can actually use Ask Earnest
 * (normal Member role has ai_usage disabled — the token budget is the
 * real cap).
 *
 * Usage:
 *   DEMO_USER_PASSWORD=<secret> pnpm tsx scripts/setup-demo-org.ts
 *
 * If DEMO_USER_PASSWORD is unset, the script generates one and prints it.
 * Save it to your env — /api/auth/demo-login reads the same variable.
 */

import 'dotenv/config';
import { DEFAULT_ROLE_PERMISSIONS } from '../shared/permissions';
import type { PermissionMatrix } from '../shared/permissions';
import {
	assertDirectusToken,
	directusRequest,
	ensureMembership,
	ensureRoles,
	ensureUser,
	findOrCreate,
	findOne,
	generatePassword,
	pingDirectus,
	randomToken,
} from './lib/demo-seed';

const DEMO_ORG_NAME = 'Earnest Demo — Solo';
const DEMO_ORG_SHORT = 'SOLO';
const DEMO_ORG_CODE = 'SOL'; // 3-letter code used as the invoice-code prefix.
const DEMO_ORG_SLUG = 'earnest-demo-solo'; // idempotency key after scripts/add-organizations-slug.ts runs.
const DEMO_USER_EMAIL = process.env.DEMO_USER_EMAIL || 'demo@earnest.guru';
const DEMO_USER_FIRST = 'Demo';
const DEMO_USER_LAST = 'Solo';
const DEMO_AI_TOKEN_LIMIT_MONTHLY = 10_000;
const DEMO_SCAN_LIMIT_MONTHLY = 3;

let demoPassword = process.env.DEMO_USER_PASSWORD || '';
let generatedPassword = false;
if (!demoPassword) {
	demoPassword = generatePassword();
	generatedPassword = true;
}

assertDirectusToken();

// Override Member.ai_usage for the demo org so visitors can chat with Earnest.
// Real-world cap is enforced by the org token budget, not the perm matrix.
const DEMO_MEMBER_PERMISSIONS: PermissionMatrix = {
	...DEFAULT_ROLE_PERMISSIONS.member,
	ai_usage: { access: true, read: true, create: true, update: false, delete: false },
};

async function ensureOrg(): Promise<any | null> {
	// Match by slug (stable URL-safe key). Fall back to legacy names so a
	// pre-slug org gets upgraded in place rather than duplicated.
	const existing =
		(await findOne<any>('organizations', { slug: { _eq: DEMO_ORG_SLUG } })) ||
		(await findOne<any>('organizations', { name: { _eq: DEMO_ORG_NAME } })) ||
		(await findOne<any>('organizations', { name: { _eq: 'Earnest Demo' } }));
	if (existing) {
		console.log(`  [skip] org "${DEMO_ORG_NAME}" (id=${existing.id})`);
		await directusRequest(`/items/organizations/${existing.id}`, 'PATCH', {
			name: DEMO_ORG_NAME,
			slug: DEMO_ORG_SLUG,
			short_name: DEMO_ORG_SHORT,
			code: DEMO_ORG_CODE,
			ai_token_limit_monthly: DEMO_AI_TOKEN_LIMIT_MONTHLY,
			scan_credits_limit_monthly: DEMO_SCAN_LIMIT_MONTHLY,
		});
		return existing;
	}
	const res = await directusRequest<any>('/items/organizations', 'POST', {
		name: DEMO_ORG_NAME,
		slug: DEMO_ORG_SLUG,
		short_name: DEMO_ORG_SHORT,
		code: DEMO_ORG_CODE,
		status: 'published',
		active: true,
		plan: 'solo',
		ai_token_limit_monthly: DEMO_AI_TOKEN_LIMIT_MONTHLY,
		ai_tokens_used_this_period: 0,
		scan_credits_limit_monthly: DEMO_SCAN_LIMIT_MONTHLY,
		scans_used_this_period: 0,
		notes: 'Public solo demo workspace. Seed regenerated by scripts/setup-demo-org.ts.',
	});
	if (!res.ok) {
		console.error(`  [fail] create org: ${res.error}`);
		return null;
	}
	console.log(`  [ok]   org "${DEMO_ORG_NAME}" (id=${res.data?.id})`);
	return res.data;
}

// ─── Seed data (solo-studio flavor) ──────────────────────────────────────

const CLIENT_SEEDS = [
	{ key: 'helios-studio', name: 'Helios Studio', industry: 'Hospitality', website: 'https://helios.example', account_state: 'active', code: 'HEL' },
	{ key: 'meridian-law', name: 'Meridian Law Group', industry: 'Legal', website: 'https://meridian.example', account_state: 'prospect', code: 'MER' },
];

async function seedClients(orgId: string): Promise<Record<string, string>> {
	const ids: Record<string, string> = {};
	for (const c of CLIENT_SEEDS) {
		const row = await findOrCreate<any>(
			'clients',
			{ _and: [{ organization: { _eq: orgId } }, { slug: { _eq: c.key } }] },
			{
				name: c.name,
				slug: c.key,
				organization: orgId,
				industry: c.industry,
				website: c.website,
				account_state: c.account_state,
				code: c.code,
				status: 'published',
				notes: 'Solo demo seed.',
			},
			`client "${c.name}"`,
		);
		if (row) ids[c.key] = row.id;
	}
	return ids;
}

