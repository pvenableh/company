async function seedInvoice(
	orgId: string,
	clientIds: Record<string, string>,
	productId: string | null,
): Promise<void> {
	const today = new Date();
	const addDays = (d: Date, days: number) => {
		const nd = new Date(d);
		nd.setDate(nd.getDate() + days);
		return nd;
	};
	const iso = (d: Date) => d.toISOString().slice(0, 10);

	const client = clientIds['helios-studio'];
	if (!client) return;

	// Format matches app/composables/useInvoices.ts::generateInvoiceCode:
	//   INV-{ORG_CODE}-{CLIENT_CODE}-{YEAR}-{NNNN}
	const year = today.getFullYear();
	const invoiceCode = `INV-${DEMO_ORG_CODE}-HEL-${year}-0042`;

	const row = await findOrCreate<any>(
		'invoices',
		{ _and: [{ bill_to: { _eq: orgId } }, { invoice_code: { _eq: invoiceCode } }] },
		{
			bill_to: orgId,
			client,
			invoice_code: invoiceCode,
			status: 'paid',
			invoice_date: iso(addDays(today, -30)),
			due_date: iso(addDays(today, -15)),
			total_amount: 18_500,
			note: 'Helios West Hotel — brand system phase 1.',
		},
		`invoice ${invoiceCode}`,
	);
	if (row && productId) {
		await findOrCreate(
			'line_items',
			{ _and: [{ invoice_id: { _eq: row.id } }, { description: { _eq: 'Brand system — phase 1' } }] },
			{
				invoice_id: row.id,
				product: productId,
				description: 'Brand system — phase 1',
				rate: 18_500,
				quantity: 1,
				amount: 18_500,
			},
			`line_item ${invoiceCode}`,
		);
	}
}

