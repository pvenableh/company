async function ensureOrg(): Promise<any | null> {
	// Match by slug (stable URL-safe key). Fall back to legacy names so a
	// pre-slug org gets upgraded in place rather than duplicated.
	const existing =
		(await findOne<any>('organizations', { slug: { _eq: DEMO_ORG_SLUG } })) ||
		(await findOne<any>('organizations', { name: { _eq: DEMO_ORG_NAME } })) ||
		(await findOne<any>('organizations', { name: { _eq: 'Earnest Demo' } }));
	if (existing) {
		console.log(`  [skip] org "${DEMO_ORG_NAME}" (id=${existing.id})`);
		await directusRequest(`/items/organizations/${existing.id}`, 'PATCH', {
			name: DEMO_ORG_NAME,
			slug: DEMO_ORG_SLUG,
			short_name: DEMO_ORG_SHORT,
			code: DEMO_ORG_CODE,
			ai_token_limit_monthly: DEMO_AI_TOKEN_LIMIT_MONTHLY,
			scan_credits_limit_monthly: DEMO_SCAN_LIMIT_MONTHLY,
			email: DEMO_ORG_EMAIL,
			phone: DEMO_ORG_PHONE,
			address: DEMO_ORG_ADDRESS,
			website: DEMO_ORG_WEBSITE,
		});
		return existing;
	}
	const res = await directusRequest<any>('/items/organizations', 'POST', {
		name: DEMO_ORG_NAME,
		slug: DEMO_ORG_SLUG,
		short_name: DEMO_ORG_SHORT,
		code: DEMO_ORG_CODE,
		status: 'published',
		active: true,
		plan: 'solo',
		ai_token_limit_monthly: DEMO_AI_TOKEN_LIMIT_MONTHLY,
		ai_tokens_used_this_period: 0,
		scan_credits_limit_monthly: DEMO_SCAN_LIMIT_MONTHLY,
		scans_used_this_period: 0,
		email: DEMO_ORG_EMAIL,
		phone: DEMO_ORG_PHONE,
		address: DEMO_ORG_ADDRESS,
		website: DEMO_ORG_WEBSITE,
		notes: 'Public solo demo workspace. Seed regenerated by scripts/setup-demo-org.ts.',
	});
	if (!res.ok) {
		console.error(`  [fail] create org: ${res.error}`);
		return null;
	}
	console.log(`  [ok]   org "${DEMO_ORG_NAME}" (id=${res.data?.id})`);
	return res.data;
}

