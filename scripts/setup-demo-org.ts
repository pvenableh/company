// ─── Main ─────────────────────────────────────────────────────────────────

async function main() {
	console.log('=========================================');
	console.log('  Earnest Demo (Solo) — org + user + seed');
	console.log('=========================================');
	console.log(`Target: ${process.env.DIRECTUS_URL || 'http://localhost:8055'}\n`);

	await pingDirectus();

	console.log('--- org ---');
	const org = await ensureOrg();
	if (!org) process.exit(1);

	console.log('\n--- system roles ---');
	const roles = await ensureRoles(org.id, { member: DEMO_MEMBER_PERMISSIONS });
	if (!roles.member) {
		console.error('No Member role id — aborting.');
		process.exit(1);
	}

	console.log('\n--- demo user ---');
	const user = await ensureUser({
		email: DEMO_USER_EMAIL,
		password: demoPassword,
		firstName: DEMO_USER_FIRST,
		lastName: DEMO_USER_LAST,
	});
	if (!user) process.exit(1);

	console.log('\n--- membership ---');
	await ensureMembership(org.id, user.id, roles.member, 'solo demo → Member');

	console.log('\n--- clients ---');
	const clientIds = await seedClients(org.id);

	console.log('\n--- contacts ---');
	const contactIds = await seedContacts(org.id, clientIds);

	console.log('\n--- leads ---');
	await seedLeads(org.id, contactIds, clientIds);

	console.log('\n--- project ---');
	const projectId = await seedProject(org.id, clientIds);

	console.log('\n--- product + invoice ---');
	const productId = await seedProduct();
	await seedInvoice(org.id, clientIds, productId);

	console.log('\n--- quick tasks ---');
	await seedQuickTasks(org.id, projectId, clientIds);

	console.log('\n=========================================');
	console.log('  Summary');
	console.log('=========================================');
	console.log(`  Org ID:  ${org.id}`);
	console.log(`  User ID: ${user.id}`);
	console.log(`  Email:   ${DEMO_USER_EMAIL}`);
	if (generatedPassword) {
		console.log('');
		console.log('  ⚠ Generated a new demo password. Save this to your env as');
		console.log('    DEMO_USER_PASSWORD — /api/auth/demo-login reads it on every request.');
		console.log(`    DEMO_USER_PASSWORD="${demoPassword}"`);
	} else {
		console.log('  Password: (from env DEMO_USER_PASSWORD)');
	}
	console.log('\nDone.');
}

main().catch((err) => {
	console.error('Setup failed:', err);
	process.exit(1);
});
